
"""
Tipos de dados int e float:

int - numeros inteiros
podem ser positivos e negativos
os numeros sem sinal são considerados positivos 



float- números de ponto flutuante ou que possuem casa decimais 
podem ser tanto positivos como negartivos
os números sem sinal são conciderados positivos
obs: o numeros de ponto flutuante usam o ponto para indeicar casa decimal e não a virgula, usando a virgula ela não indicara
casa decimal e na 'saida do interpretador aparece-rá somente um espaço'. 


função para descobrir o tipo de dados

ex: print(type(o dado: str, int, float, boolenao)) > na saida do interpretador ira mostra a class que o dados inserido pertence.
"""





